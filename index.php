<p><a href="http://littlegamesblog.wordpress.com/" target="_blank"><img alt="JoelyBean" src="http://images.jayisgames.com/icon_Joel.gif" align="left" class="leftalphaimage" width="50" height="50" /></a>You wake to find yourself in a strange place. This bed is not your own; you've never even seen this house before. The small room is empty save for an old computer, desk and bookshelf. Warm light filters in through a foggy window, bathing everything in a pinkish hue. The clean hardwood floor is partly covered by a soft looking purple carpet. This place is beautiful, but unsettling. And this is the first view you'll have of the breathtaking world of <a href="http://oneshot.cu.cc/" target="_blank">Oneshot</a>. Made for the 2014 Indie Game Maker contest and downloadable free for PC, Mac and Linux, Oneshot is a <a href="/tag/puzzle">puzzle-based</a> <a href="/tag/adventure">adventure</a> which plays only in windowed mode, and yes, there is a reason for that. No, it's not a room escape, though the game's first few minutes would certainly have you believe so. It's not a horror game, but it is certainly atmospheric and has a few untraditionally "creepy" moments. The game's co-developers, <a href="/tag/mvelasquez">Mathew Velasquez</a> and <a href="/tag/nightmargin">Casey Gu</a>, describe the experience as a mission to "guide a lost child through a strange world, utilizing items, characters, and the environment to progress." That could describe most any adventure game. With such an intentionally vague description, it is difficult for any player to have any idea what this game is really about&mdash;and <em>that's the point</em>. This is a game about exploration and mystery, the kind of experience you're meant to have with as little prior knowledge as possible. Don't even read any comments before you start, or it will be spoiled for you. All the information you need is the controls: move your character with the [arrow] keys, perform actions and use items with [Z] or [spacebar], and use [X] to open the menu. Now go play it. Oh, one last thing: put some time aside, because once you begin, you can't stop playing it for at least a little while. Luckily, this reviewer's hardware is sturdy enough that I was able to see everything through to the end, but other players have told horror stories of computers shutting off mid-game and losing everything. There are three moments throughout the game where you can save and quit safely, and you'll know them when you find them, but that's it. </p>
